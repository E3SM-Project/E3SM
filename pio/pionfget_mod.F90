       case(iotype_netcdf)
          ! Only io proc 0 will do reading
          if (File%iosystem%io_rank == 0) then
             ierr = nf90_get_var(File%fh, varid, ival, start=start, count=count)
          end if
#endif
       end select
    end if
    call check_netcdf(File,ierr,_FILE_,__LINE__)

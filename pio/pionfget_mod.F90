    type (File_desc_t), intent(in) :: File
    integer, intent(in) :: varid
    character(len=*), intent(out) :: ival

    character(len=*), parameter :: subName=modName//'::get_var_0d_text'
    integer :: iotype, mpierr
    integer(kind=PIO_OFFSET) :: isize

#ifdef TIMING
    call t_startf("pio_get_var_0d_text")
#endif
    ierr=0
    iotype = File%iotype 
    isize=1	
#if (0 > 0)
    isize= size(ival)
#endif
#if (100 == TYPETEXT)
    isize = isize*len(ival)	
    ival = ' '
#endif

    if(File%iosystem%IOProc) then
       select case (iotype) 
#ifdef _PNETCDF
       case(iotype_pnetcdf)
          ierr = nfmpi_get_var_all(File%fh, varid, ival, isize, MPI_CHARACTER)
#endif
#ifdef  _NETCDF
       case(pio_iotype_netcdf4p, pio_iotype_netcdf4c)
             ierr = nf90_get_var(File%fh, varid, ival)
       case(iotype_netcdf)
          ! Only io proc 0 will do reading
          if (File%iosystem%io_rank == 0) then
             ierr = nf90_get_var(File%fh, varid, ival)
          end if
#endif
       end select
    end if
    call check_netcdf(File,ierr,_FILE_,__LINE__)
    if(iotype.eq.iotype_netcdf .or. File%iosystem%num_iotasks < File%iosystem%num_tasks) then
       call MPI_Bcast(ival,int(isize), MPI_CHARACTER , File%iosystem%IOMaster, File%iosystem%Comp_comm, mpierr)
       call CheckMPIReturn(subName, mpierr)
    end if
#ifdef TIMING
    call t_stopf("pio_get_var_0d_text")
#endif
  end function get_var_0d_text

